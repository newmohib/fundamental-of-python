

# arithmetic Operations

print(10 + 3) # addition operator
print(10 - 3) # subtracting operator
print(10 * 3) # multiplication operator
print(10 / 3) # division operator
print(10 // 3) # Floor division operator
print(10 ** 3) # Exponentiation/power operator
print(10 % 3) # Modulus operator

x = 10
x += 3 # augmented assignment operator
print(x)
x -= 5 # augmented assignment operator
print(x)
x *= 4 # augmented assignment operator
print(x)


# Operator Precedence

y = 10 + 3 * 2 ** 2
print(y) #22

y = (10 + 3) * 2 ** 2
print(y) #52
# Order of Operation

# 0: parenthesis
# 1: exponentiation 2 ** 3
# 2: multiplication or division
# 3: addition or subtraction





